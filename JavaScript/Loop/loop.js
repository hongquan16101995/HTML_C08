//cấu trúc for
let i = 0
for (; i < 10;) {
    console.log("Đây là lần lặp thứ: " + (i) + ", giá trị của i: " + i)
    i++
}

console.log("-----------------")

//cấu trúc while
let j = 0
while (j > 10) {
    console.log("Đây là lần lặp thứ: " + (j) + ", giá trị của i: " + j)
    j++
}

console.log("-----------------")

//cấu trúc do..while
let h = 0
do {
    console.log("Đây là lần lặp thứ: " + (h) + ", giá trị của i: " + h)
    h++
} while (h > 10)
